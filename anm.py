def f(x):
    """
    Define your function here.
    Change this to any equation you want to solve (= 0).

    Examples:
        return x**3 - x - 2
        return math.cos(x) - x
        return x**2 - 2
        return math.exp(x) - 3*x
    """
    return x**3 - x - 2


def bisection(a, b, tol, max_iter=50, criterion="interval"):
    """
    Bisection Method to find root of f(x) = 0 in [a, b].

    Parameters:
        a         : lower bound of interval
        b         : upper bound of interval
        tol       : tolerance (stopping condition)
        max_iter  : maximum number of iterations (default 50)
        criterion : stopping criterion
                    'interval' → |b - a| < tol
                    'fx'       → |f(c)| < tol
                    'both'     → both conditions met

    Returns:
        root      : approximate root
        iterations: number of iterations performed
    """

    fa = f(a)
    fb = f(b)

    # ── Pre-checks ──────────────────────────────
    if fa * fb > 0:
        print("\n  ERROR: f(a) and f(b) must have opposite signs.")
        print(f"  f({a}) = {fa:.6f}")
        print(f"  f({b}) = {fb:.6f}")
        print("  No guaranteed root in this interval.\n")
        return None, 0

    if fa == 0:
        print(f"\n  f(a) = 0 exactly. Root is a = {a}")
        return a, 0

    if fb == 0:
        print(f"\n  f(b) = 0 exactly. Root is b = {b}")
        return b, 0

    # ── Table Header ─────────────────────────────
    print("\n" + "=" * 90)
    print("  BISECTION METHOD")
    print("=" * 90)
    print(f"  Interval  : [{a}, {b}]")
    print(f"  f(a)      : {fa:.6f}")
    print(f"  f(b)      : {fb:.6f}")
    print(f"  Tolerance : {tol}")
    print(f"  Criterion : {criterion}")
    print(f"  Max Iter  : {max_iter}")
    print("=" * 90)
    print(f"{'Iter':>4}  {'a':>12}  {'b':>12}  {'c=(a+b)/2':>14}  "
          f"{'f(a)':>12}  {'f(c)':>12}  {'|b-a|':>12}  {'Error≈':>12}")
    print("-" * 90)

    root = None
    n    = 0

    for i in range(1, max_iter + 1):
        n  = i
        c  = (a + b) / 2.0
        fa = f(a)
        fc = f(c)
        interval_width = abs(b - a)
        error          = interval_width / 2.0

        print(f"{i:>4}  {a:>12.6f}  {b:>12.6f}  {c:>14.8f}  "
              f"{fa:>12.6f}  {fc:>12.6f}  {interval_width:>12.8f}  {error:>12.8f}")

        # ── Stopping Criteria ───────────────────
        if criterion == "interval":
            converged = interval_width < tol
        elif criterion == "fx":
            converged = abs(fc) < tol
        else:  # both
            converged = interval_width < tol and abs(fc) < tol

        if converged:
            root = c
            break

        # ── Update Bracket ──────────────────────
        if fa * fc < 0:
            b = c
        else:
            a = c

        root = c  # best estimate so far

    return root, n


def get_criterion():
    print("\n  Stopping criterion options:")
    print("    1 → |b - a| < tolerance  (interval width)")
    print("    2 → |f(c)| < tolerance   (function value)")
    print("    3 → Both conditions")
    choice = input("  Choose [1/2/3] (default 1): ").strip()
    return {"2": "fx", "3": "both"}.get(choice, "interval")


