import math

# ─────────────────────────────────────────────────────
#  Secant Method — Applied Numerical Methods
# ─────────────────────────────────────────────────────
#
#  Finds root of f(x) = 0 using two initial guesses.
#  Does NOT require derivative of f(x).
#
#  Formula:
#      x_(n+1) = x_n - f(x_n) * (x_n - x_(n-1))
#                              / (f(x_n) - f(x_(n-1)))
#
#  Convergence order: ~1.618 (superlinear)
# ─────────────────────────────────────────────────────


def f(x):
    """
    Define your function f(x) here.
    The method finds x such that f(x) = 0.

    Examples:
        return x**3 - x - 2
        return math.cos(x) - x
        return x**2 - 2
        return math.exp(x) - 3*x
        return x**3 - 4*x + 1
        return math.log(x) - math.cos(x)
    """
    return x**3 - x - 2


def secant(x0, x1, tol, max_iter=50, criterion="both"):
    """
    Secant Method to find root of f(x) = 0.

    Parameters:
        x0        : first initial guess
        x1        : second initial guess
        tol       : tolerance (stopping condition)
        max_iter  : maximum number of iterations (default 50)
        criterion : stopping criterion
                    'absolute' → |x_(n+1) - x_n| < tol
                    'relative' → |x_(n+1) - x_n| / |x_(n+1)| < tol
                    'fx'       → |f(x_(n+1))| < tol
                    'both'     → absolute error AND |f(x)| < tol

    Returns:
        root       : approximate root
        iterations : number of iterations performed
    """

    f0 = f(x0)
    f1 = f(x1)

    # ── Table Header ─────────────────────────────
    print("\n" + "=" * 100)
    print("  SECANT METHOD")
    print("=" * 100)
    print(f"  First guess   x0 = {x0}   →  f(x0) = {f0:.8f}")
    print(f"  Second guess  x1 = {x1}   →  f(x1) = {f1:.8f}")
    print(f"  Tolerance        : {tol}")
    print(f"  Criterion        : {criterion}")
    print(f"  Max Iterations   : {max_iter}")
    print("=" * 100)
    print(f"{'Iter':>4}  {'x_(n-1)':>14}  {'x_n':>14}  {'x_(n+1)':>14}  "
          f"{'f(x_n)':>12}  {'f(x_(n+1))':>12}  {'|x_(n+1)-x_n|':>14}  {'Status':>8}")
    print("-" * 100)

    # Print iteration 0 as starting state
    print(f"{'0':>4}  {'—':>14}  {x0:>14.8f}  {x1:>14.8f}  "
          f"{f0:>12.6f}  {f1:>12.6f}  {'—':>14}  {'':>8}")

    root  = None
    iters = 0

    x_prev = x0
    x_curr = x1

    for i in range(1, max_iter + 1):
        iters = i

        f_prev = f(x_prev)
        f_curr = f(x_curr)

        # ── Denominator check ────────────────────
        denom = f_curr - f_prev
        if abs(denom) < 1e-14:
            print(f"\n  ERROR at iteration {i}: f(x_n) - f(x_(n-1)) ≈ 0")
            print("  Division by zero encountered. Try different initial guesses.\n")
            return None, i

        # ── Secant formula ───────────────────────
        x_next = x_curr - f_curr * (x_curr - x_prev) / denom
        f_next = f(x_next)

        abs_err = abs(x_next - x_curr)
        rel_err = abs_err / abs(x_next) if abs(x_next) > 1e-12 else float('inf')

        # ── Divergence check ─────────────────────
        if abs(x_next) > 1e10 or math.isnan(x_next) or math.isinf(x_next):
            print(f"{i:>4}  {x_prev:>14.8f}  {x_curr:>14.8f}  {x_next:>14.4e}  "
                  f"{f_curr:>12.6f}  {f_next:>12.4e}  {abs_err:>14.4e}  {'DIVERGED':>8}")
            print("\n  ✗ Method is DIVERGING. Try different initial guesses.\n")
            return None, i

        # ── Stopping Criteria ────────────────────
        if criterion == "absolute":
            converged = abs_err < tol
        elif criterion == "relative":
            converged = rel_err < tol
        elif criterion == "fx":
            converged = abs(f_next) < tol
        else:  # both
            converged = abs_err < tol and abs(f_next) < tol

        status = "✓ DONE" if converged else ""

        print(f"{i:>4}  {x_prev:>14.8f}  {x_curr:>14.8f}  {x_next:>14.8f}  "
              f"{f_curr:>12.6f}  {f_next:>12.6f}  {abs_err:>14.8f}  {status:>8}")

        if converged:
            root = x_next
            break

        x_prev = x_curr
        x_curr = x_next
        root   = x_next  # best estimate so far

    return root, iters


def get_criterion():
    print("\n  Stopping criterion options:")
    print("    1 → |x_(n+1) - x_n| < tolerance         (absolute error)")
    print("    2 → |x_(n+1) - x_n| / |x_(n+1)| < tol  (relative error)")
