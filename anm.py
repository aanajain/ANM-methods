import math

# ─────────────────────────────────────────────────────────────────
#  Newton's Interpolation Method — Applied Numerical Methods
#  Covers:
#    1. Newton's Forward Difference Interpolation
#    2. Newton's Backward Difference Interpolation
# ─────────────────────────────────────────────────────────────────
#
#  Forward  formula (use when x is near the BEGINNING of the table)
#  Backward formula (use when x is near the END of the table)
#
#  Both use equally spaced data points.
# ─────────────────────────────────────────────────────────────────


# ══════════════════════════════════════════════════════════════════
#  DIFFERENCE TABLE
# ══════════════════════════════════════════════════════════════════

def build_difference_table(y):
    """
    Builds the full finite difference table from y values.
    Returns a 2D list where diff[i][j] = j-th order difference at index i.
    diff[i][0] = y[i]
    """
    n = len(y)
    diff = [[0.0] * n for _ in range(n)]

    for i in range(n):
        diff[i][0] = y[i]

    for j in range(1, n):
        for i in range(n - j):
            diff[i][j] = diff[i + 1][j - 1] - diff[i][j - 1]

    return diff


def print_difference_table(x, y, diff):
    """Prints the forward difference table in a neat format."""
    n = len(x)
    h = x[1] - x[0]

    print("\n" + "=" * (16 * (n + 1)))
    print("  FORWARD DIFFERENCE TABLE")
    print("=" * (16 * (n + 1)))

    # Header
    header = f"{'x':>10}  {'y = f(x)':>12}"
    for j in range(1, n):
        header += f"  {'Δ' * j + 'y':>12}"
    print(header)
    print("-" * (16 * (n + 1)))

    # Rows
    for i in range(n):
        row = f"  {x[i]:>8.4f}  {diff[i][0]:>12.6f}"
        for j in range(1, n - i):
            row += f"  {diff[i][j]:>12.6f}"
        print(row)

    print("=" * (16 * (n + 1)))


# ══════════════════════════════════════════════════════════════════
#  FORWARD INTERPOLATION
# ══════════════════════════════════════════════════════════════════

def newton_forward(x, y, x_val):
    """
    Newton's Forward Difference Interpolation.
    Best used when x_val is near the START of the table.

    Formula:
        y(x) = y0 + s*Δy0 + s(s-1)/2! * Δ²y0 + ...
        where s = (x - x0) / h
    """
    n    = len(x)
    h    = x[1] - x[0]
    diff = build_difference_table(y)

    x0 = x[0]
    s  = (x_val - x0) / h

    print("\n" + "─" * 60)
    print("  NEWTON'S FORWARD INTERPOLATION")
    print("─" * 60)
    print(f"  x0 = {x0},  h = {h},  x = {x_val}")
    print(f"  s  = (x - x0) / h = ({x_val} - {x0}) / {h} = {s:.6f}")
    print("─" * 60)
    print(f"  {'Term':>4}  {'Δⁿy₀':>14}  {'s-factor':>20}  {'Contribution':>14}")
    print("  " + "-" * 56)

    result    = diff[0][0]
    s_factor  = 1.0
    factorial = 1.0

    print(f"  {'0':>4}  {diff[0][0]:>14.6f}  {'1':>20}  {diff[0][0]:>14.6f}")

    for k in range(1, n):
        s_factor  *= (s - (k - 1))
        factorial *= k
        term       = (s_factor / factorial) * diff[0][k]
        result    += term

        s_expr = f"s(s-1)...(s-{k-1})" if k > 1 else "s"
        print(f"  {k:>4}  {diff[0][k]:>14.6f}  {s_expr + f'/{k}!' :>20}  {term:>14.6f}")

    return result


# ══════════════════════════════════════════════════════════════════
#  BACKWARD INTERPOLATION
# ══════════════════════════════════════════════════════════════════

def newton_backward(x, y, x_val):
    """
    Newton's Backward Difference Interpolation.
    Best used when x_val is near the END of the table.

    Formula:
        y(x) = yn + s*∇yn + s(s+1)/2! * ∇²yn + ...
        where s = (x - xn) / h
    """
    n    = len(x)
    h    = x[1] - x[0]
    diff = build_difference_table(y)

    xn = x[n - 1]
    s  = (x_val - xn) / h

    print("\n" + "─" * 60)
    print("  NEWTON'S BACKWARD INTERPOLATION")
    print("─" * 60)
    print(f"  xn = {xn},  h = {h},  x = {x_val}")
    print(f"  s  = (x - xn) / h = ({x_val} - {xn}) / {h} = {s:.6f}")
    print("─" * 60)
    print(f"  {'Term':>4}  {'∇ⁿyₙ':>14}  {'s-factor':>20}  {'Contribution':>14}")
    print("  " + "-" * 56)

    result    = diff[n - 1][0]
    s_factor  = 1.0
    factorial = 1.0

    print(f"  {'0':>4}  {diff[n-1][0]:>14.6f}  {'1':>20}  {diff[n-1][0]:>14.6f}")

    for k in range(1, n):
        s_factor  *= (s + (k - 1))
        factorial *= k
        term       = (s_factor / factorial) * diff[n - 1 - k][k]
        result    += term

        s_expr = f"s(s+1)...(s+{k-1})" if k > 1 else "s"
        print(f"  {k:>4}  {diff[n-1-k][k]:>14.6f}  {s_expr + f'/{k}!':>20}  {term:>14.6f}")

    return result


