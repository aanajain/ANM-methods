def validate_equal_spacing(x):
    """Checks that x values are equally spaced."""
    h = x[1] - x[0]
    for i in range(1, len(x) - 1):
        if abs((x[i + 1] - x[i]) - h) > 1e-9:
            return False, h
    return True, h


