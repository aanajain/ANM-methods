import math

# ─────────────────────────────────────────────────────
#  Fixed Point Iteration Method — Applied Numerical Methods
# ─────────────────────────────────────────────────────
#
#  Solves f(x) = 0 by rewriting as x = g(x)
#  Then iterating:  x_(n+1) = g(x_n)
#
#  Convergence condition: |g'(x)| < 1 near the root
# ─────────────────────────────────────────────────────


def g(x):
    """
    Iteration function g(x) such that f(x) = 0 ⟺ x = g(x)

    Change this to match your equation.

    Examples (equation → rearranged g(x)):
        f(x) = x³ - x - 2         →  g(x) = (x + 2) ** (1/3)
        f(x) = cos(x) - x         →  g(x) = math.cos(x)
        f(x) = x² - 2             →  g(x) = 2 / x          (or (x + 2/x)/2 for Newton-like)
        f(x) = e^x - 3x           →  g(x) = math.exp(x) / 3
        f(x) = x³ - 4x + 1       →  g(x) = (x**3 + 1) / 4
    """
    return (x + 2) ** (1/3)


def f(x):
    """
    Original equation f(x) = 0
    Used only to display f(x_n) in the table for verification.
    Change this to match your equation.
    """
    return x**3 - x - 2


def fixed_point_iteration(x0, tol, max_iter=50, criterion="both"):
    """
    Fixed Point Iteration to find root of f(x) = 0
    by solving x = g(x).

    Parameters:
        x0        : initial guess
        tol       : tolerance (stopping condition)
        max_iter  : maximum number of iterations (default 50)
        criterion : stopping criterion
                    'absolute' → |x_(n+1) - x_n| < tol
                    'relative' → |x_(n+1) - x_n| / |x_(n+1)| < tol
                    'fx'       → |f(x_n)| < tol
                    'both'     → |x_(n+1) - x_n| < tol AND |f(x_n)| < tol

    Returns:
        root       : approximate root
        iterations : number of iterations performed
    """

    # ── Table Header ─────────────────────────────
    print("\n" + "=" * 95)
    print("  FIXED POINT ITERATION METHOD")
    print("=" * 95)
    print(f"  Initial Guess : x0 = {x0}")
    print(f"  g(x0)         : {g(x0):.8f}")
    print(f"  f(x0)         : {f(x0):.8f}")
    print(f"  Tolerance     : {tol}")
    print(f"  Criterion     : {criterion}")
    print(f"  Max Iter      : {max_iter}")
    print("=" * 95)
    print(f"{'Iter':>4}  {'x_n':>14}  {'x_(n+1)=g(x_n)':>16}  "
          f"{'f(x_n)':>12}  {'|x_(n+1)-x_n|':>14}  {'Rel. Error':>12}  {'Status':>10}")
    print("-" * 95)

    x_n   = x0
    root  = None
    iters = 0

    for i in range(1, max_iter + 1):
        iters = i

        try:
            x_next = g(x_n)
        except (ValueError, ZeroDivisionError, OverflowError) as e:
            print(f"\n  ERROR at iteration {i}: g(x) undefined — {e}")
            print("  Try a different initial guess or rewrite g(x).\n")
            return None, i

        fx_n      = f(x_n)
        abs_err   = abs(x_next - x_n)
        rel_err   = abs_err / abs(x_next) if abs(x_next) > 1e-12 else float('inf')

        # ── Divergence check ────────────────────
        if abs(x_next) > 1e10:
            status = "DIVERGED"
            print(f"{i:>4}  {x_n:>14.8f}  {x_next:>16.8f}  "
                  f"{fx_n:>12.6f}  {abs_err:>14.8f}  {rel_err:>12.6e}  {status:>10}")
            print("\n  ✗ Method is DIVERGING. Try a different g(x) or initial guess.\n")
            return None, i

        # ── Stopping Criteria ───────────────────
        if criterion == "absolute":
            converged = abs_err < tol
        elif criterion == "relative":
            converged = rel_err < tol
        elif criterion == "fx":
            converged = abs(fx_n) < tol
        else:  # both
            converged = abs_err < tol and abs(f(x_next)) < tol

        status = "✓ DONE" if converged else ""

        print(f"{i:>4}  {x_n:>14.8f}  {x_next:>16.8f}  "
              f"{fx_n:>12.6f}  {abs_err:>14.8f}  {rel_err:>12.6e}  {status:>10}")

        if converged:
            root = x_next
            break

        x_n = x_next
        root = x_next  # best estimate so far

    return root, iters


def get_criterion():
    print("\n  Stopping criterion options:")
    print("    1 → |x_(n+1) - x_n| < tolerance         (absolute error)")
    print("    2 → |x_(n+1) - x_n| / |x_(n+1)| < tol  (relative error)")
    print("    3 → |f(x_n)| < tolerance                 (function value)")
    print("    4 → Both absolute error AND |f(x)| < tol")
    choice = input("  Choose [1/2/3/4] (default 4): ").strip()
    return {"1": "absolute", "2": "relative", "3": "fx"}.get(choice, "both")


